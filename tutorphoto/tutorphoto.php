      <h2>#No.3 Pencahayaan</h2>
      <center><img src="images/Pencahayaan.jpg" style="height:auto;width:auto;"></img></center>
	   <h3>Apa itu <i>Pencahayaan?</i></h3>
      <p>Dalam dunia <strong>Fotografi</strong>, <strong>Pencahayaan</strong>, berarti mengacu pada bagaimana cara memanfaatkan sumber 
	     cahaya pada saat pengambilan gambar. Mulai dari sumber cahaya alami, yaitu sinar matahari, hingga sumber cahaya buatan yang berasal dari perangkat lampu.
	  </p>
	  <p>Pencahayaan juga merupakan salah satu faktor utama yang bisa membuat gambar yang bagus atau jelek. Mari kita bicara tentang bagaimana 
	     kita bisa memanfaatkan pencahayaan dengan sebaik-baiknya untuk mendapatkan hasil terbaik dari suatu bidikan.
	  </p>
	  <h3>
		“Exposure” (Pencahayaan) mengacu ke kecerahan dalam gambar sebagaimana ditentukan oleh interaksi antara aperture dan kecepatan rana
	  </h3>
	  <h4>
		Hal yang perlu dicatat :
	  </h4>
	  <p>
		&nbsp;&nbsp;&nbsp;- Kamera dilengkapi dengan fungsi Auto exposure (AE).
		<br>&nbsp;&nbsp;&nbsp;- Kita mengacu ke kecerahan yang dihitung dan ditetapkan oleh kamera sebagai “correct exposure” (pencahayaan tepat).
		<br>&nbsp;&nbsp;&nbsp;- Jika pencahayaan yang tepat tidak memuaskan, Anda bisa menggunakan exposure compensation (kompensasi 
		&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;pencahayaan) untuk menyesuaikannya.
	  </p>
	  <p>
		Kata "exposure" (pencahayaan) mengacu ke volume cahaya yang diambil saat gambar dibidik, yang memengaruhi kecerahan gambar yang dihasilkan.
		Volume cahaya ini pada dasarnya ditentukan oleh kombinasi pengaturan aperture dan kecepatan rana.
	  </p>
	  <p>
		Kamera DSLR dilengkapi dengan fungsi Auto exposure (AE). Oleh karena itu, dalam kondisi normal, di mana kecepatan ISO konstan,
		Anda tidak perlu memikirkan tentang pengaturan aperture atau kecepatan rana yang akan memberi Anda pencahayaan memadai, karena 
		ini akan secara otomatis ditetapkan oleh kamera. Kita bisa mendapatkan hasil yang bagus pada segala macam pemandangan dan subjek 
		dengan pencahayaan yang ditetapkan secara otomatis ini, yang kita sebut “correct exposure” (pencahayaan tepat).
	  </p>
	  <p>
		Namun demikian, pencahayaan tepat mungkin bukan merupakan pencahayaan yang optimal untuk suatu pemandangan, karena tergantung 
		pada kondisi pemandangan dan subjeknya, ada kalanya kecerahan pemandangan tidak sesuai dengan yang kita harapkan. Apabila hal 
		ini terjadi, kita selalu dapat memanfaatkan fitur kompensasi pencahayaan untuk menyesuaikan level kecerahan. Apabila kita ingin
		agar gambarnya terlihat lebih gelap, kita bisa menetapkan nilai kompensasi pencahayaan negatif (“-“). Jika kita ingin gambar 
		yang lebih cerah, kita bisa menetapkan nilai positif (“+“).
	  </p>
	  <p>
		Bahkan, apabila mengambil gambar dengan situasi yang sama, atau subjek yang sama, penyesuaian sederhana pada pencahayaan 
		bisa memberi Anda hasil yang sangat berbeda. Dengan kata lain, seni kompensasi pencahayaan adalah sesuatu yang kita semua 
		harus pelajari untuk mengambil gambar yang bagus.
	  </p>
