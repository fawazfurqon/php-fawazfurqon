      <h2>#No.2 Shutter Speed</h2>
      <center><img src="images/shutterspeed.jpg" style="height:auto;width:auto;"></img></center>
	  <h3>Apa itu <i>Shutter Speed?</i></h3>
	  <h4>
		Kecepatan rana membantu Anda "mengontrol" pergerakan subjek dalam foto Anda
	  </h4>
      <p><strong>Shutter Speed</strong> Atau <strong>Rana</strong> adalah semacam tirai yang ada di depan lembaran
 	     film sebagai media perekam yang bekerja dengan prinsip membuka dan menutup seperti daun pintu.
	  </p>
	  <p>Ketika memotret, Anda tentu ingin memahami tentang kecepatan rana serta efeknya yang baik pada foto Anda.
	     Efek semacam apakah yang bisa Anda ciptakan pada kecepatan rana yang lebih pesat atau lebih lambat? Mari kita
	     cermati efek dari kecepatan rana yang berbeda-beda dengan bantuan contoh berikut ini.
	  </p>
	  <h3>
		Kecepatan rana membantu Anda "mengontrol" pergerakan subjek dalam foto Anda
	  </h3>
	  <h4>
		Hal yang perlu dicatat :
	  </h4>
	  <p>
		&nbsp;&nbsp;&nbsp;- Kecepatan rana yang lebih pesat, membekukan subjek yang sedang bergerak.
		<br>&nbsp;&nbsp;&nbsp;- Kecepatan rana yang lebih lambat menciptakan efek buram gerakan dari pergerakan subjek.
		<br>&nbsp;&nbsp;&nbsp;- Anda bisa menyesuaikan jumlah cahaya, dengan membuka/menutup rana.
	  </p>
	  <p>
		Kecepatan rana (juga: waktu pencahayaan) adalah lama waktu ketika rana terbuka dan cahaya memasuki sensor gambar di dalam kamera. 
		Kecepatan rana ditunjukkan sebagai 1 det., 1/2 det., 1/4 det. ... 1/125 det. hingga 1/250 det., dll.
	  </p>
	  <p>
		Kecepatan rana yang lebih pesat mengurangi lama waktu di mana cahaya bisa masuk, sedangkan kecepatan rana yang lebih lambat, 
		menambah panjang waktu ini. Oleh karena itu, semakin lambat kecepatan rana, semakin besar jumlah cahaya yang bisa masuk ke kamera.
	  </p>
	  <p>
		Kecepatan rana tidak hanya memungkinkan Anda mengubah jumlah cahaya, tapi juga bisa mengubah cara pergerakan subjek yang ditangkap.
		Pada kecepatan rana yang lebih pesat, Anda bisa membekukan sepenuhnya gerakan subjek yang sedang bergerak. Sebaliknya, apabila 
		menggunakan kecepatan rana yang lebih lambat, Anda bisa memburamkan subjek pada arah gerakan, dan menangkap gerakan subjek, seperti 
		air yang mengalir. Dengan kata lain, kecepatan rana memungkinkan Anda mengontrol cara pergerakan subjek fotografis yang diambil.
	  </p>
