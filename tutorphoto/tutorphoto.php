      <h2>#No.1 Aperture</h2>
      <center><img src="images/aperture.jpg" style="height:auto;width:auto;"></img></center>
	  <h3>Apa itu <i>Aperture?</i></h3> 
	  <p><strong>Aperture</strong> Dalam optika, Aperture adalah bilangan yang menunjukkan korelasi panjang fokus lensa terhadap tingkap. 
	     Sebagai contoh, lensa dengan panjang fokus 100mm, pada pengaturan bukaan 4, mempunyai arti bahwa diafragma pada lensa tersebut
		 sedang terbuka dengan diameter 25mm. Biasanya dilambangkan dengan huruf f.
      </p>
	  <p>Hal pertama yang harus dipertimbangkan apabila mengambil foto dengan kamera digital yaitu efek aperture pada gambar Anda. 
	     Bagaimanakah hasil perubahan pada foto akhir bergantung pada aperture-nya, apakah dilebarkan atau disempitkan? Dalam artikel 
	     ini, kami mempelajari efek dari beragam aperture mengenai depth-of-field dengan membandingkan beberapa contoh, dan mempelajari tentang konsep f-stop
	  </p>
	  <h3>
		Aperture mengontrol jumlah cahaya yang memasuki lensa
	  </h3>
	  <h4>
		Hal yang perlu dicatat :
	  </h4>
	  <p>
		&nbsp;&nbsp;&nbsp;- Semakin besar aperture (yaitu, semakin kecil f-number), semakin besar bokeh-nya.
		<br>&nbsp;&nbsp;&nbsp;- Semakin kecil aperture (yaitu, semakin besar f-number), semakin besar area dalam fokus (depth-of-field).
		<br>&nbsp;&nbsp;&nbsp;- Jumlah cahaya yang memasuki sensor bisa dikontrol dengan melebarkan menyempitkan aperture.
	  </p>
	  <p>
		Aperture memungkinkan kita mengontrol jumlah cahaya yang memasuki lensa. Apabila aperture dilebarkan, semakin banyak cahaya yang bisa 
		masuk, dan sebaliknya, apabila aperture disempitkan, semakin kurang cahaya yang bisa masuk ke lensa. Nilai numerik dalam perbedaan ukuran 
		aperture, dikenal sebagai f-number. f-number standar yaitu: f/1.4, f/2, f/2.8, f/4, f/5.6, f/8… dll. Melebarkan aperture akan mengurangi
		f-number, sedangkan menyempitkan aperture akan menambahnya.
	  </p>
	  <p>
		Apabila f-number berubah, maka, yang berubah bukan hanya jumlah cahaya yang memasuki kamera, melainkan juga ukuran area dalam gambar yang tampak 
		dalam fokus. Semakin kecil f-number, semakin kecil pula area gambar dalam fokus. Sebaliknya, semakin besar f-number, semakin besar pula area 
		gambar dalam fokus. f-number besar menghasilkan foto yang tajam seluruhnya hingga ke latar belakang.
	  </p>
	  <p>
		Pada f-number terkecil, Anda mendapatkan "aperture maksimum". Hal ini memungkinkan masuknya sejumlah cahaya yang sebesar-besarnya,
		dan juga apabila Anda bisa mencapai bokeh yang paling menonjol ("terbesar").
	  </p>
	
