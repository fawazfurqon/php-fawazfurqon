      <h2>#No.6 White Balance</h2>
      <center><img src="images/whitebalances.jpg" style="height:auto;width:auto;"></img></center>
	  <h3>Apa itu <i>White Balance?</i></h3>
       <p><b>White Balance</b> adalah istilah dalam fotografi untuk kalibrasi titik berwarna putih. Saat masih era fotografi film, 
	      white balance ditentukan pada film godzilla apa yang kita gunakan. Di era itu kita hanya dihadapkan pada 2 pilihan jenis film yaitu daylight atau tungsten
	  </p>
	  <p><b>White Balance</b> juga merupakan fitur yang memastikan, bahwa warna putih direproduksi secara akurat, 
	     apa pun jenis kondisi pencahayaan saat foto itu diambil. Pada tingkat yang paling dasar, adalah 
	     hal yang umum untuk menggunakan pengaturan Auto White Balance. Namun demikian, pengaturan ini bukan
	     solusi yang sesuai untuk semuanya. Untuk pengaturan white balance yang paling sesuai dengan sumber
	     pencahayaan, pilih salah satu pengaturan preset white balance pada kamera Anda.
	  </p>
	  <h3>
		White balance memastikan bahwa hasil gambar Anda akan bagus dengan nada warna yang sesuai untuk pencahayaan yang digunakan
	  </h3>
	  <p>
		<b>Hal yang perlu dicatat :</b>
	  </p>
	  <p>
		&nbsp;&nbsp;&nbsp;&nbsp;- Fungsi semula adalah untuk memastikan bahwa warna putih tampak putih dalam foto Anda.
		<br>&nbsp;&nbsp;&nbsp;&nbsp;- Anda juga dapat menggunakannya untuk menambah torehan warna ke foto Anda.
	  </p>
	  <P>
		Tergantung pada sumber cahayanya, foto benda putih yang Anda ambil dapat mengambil torehan warna yang tampak 
		kemerah-merahan atau kebiru-biruan, misalnya. Ini adalah sesuatu yang tidak nyata secara kasat mata, karena 
		otak kita secara otomatis mengoreksi torehan warna, sehingga benda warna putih masih tampak putih apa pun 
		sumber cahayanya. Namun demikian, kamera tidak memiliki kemampuan itu. Alih-alih, fungsi ini dilaksanakan oleh 
		fungsi white balance (WB), yang memastikan bahwa benda putih digambarkan sebagai benda berwarna putih, apa pun sumber cahayanya.
	  </p>
	  <p>
		Sering kali, jika Anda membidik dengan white balance yang ditetapkan ke mode "Auto", yang secara umum juga dikenal 
		sebagai Auto White Balance (AWB), warna dalam foto akan mendekati warna yang Anda lihat secara kasat mata. Namun 
		demikian, untuk sebagian pemandangan, "Auto" tidak dapat melakukan koreksi yang sesuai, sehingga menghasilkan warna 
		yang digambarkan secara berbeda dari yang Anda lihat. Apabila hal ini terjadi, pilih pengaturan white balance di antara
		opsi preset. Anda akan memiliki beberapa opsi, misalnya “Daylight”, “Shade”, “Cloudy”, “Tungsten light”, “White fluorescent light” dan sebagainya.
	  </p>
	  <p>
		Namun demikian, Anda juga dapat menggunakan fungsi WB untuk secara sengaja menambahkan torehan warna ke foto Anda. Cobalah sendiri: 
		Pertama-tama, ambil foto dengan preset "Daylight". Bandingkan dengan foto yang diambil dengan “White fluorescent light” atau “Tungsten 
		light”. Lihat, apakah nada warna gambar tampak lebih kebiru-biruan daripada yang dibidik dengan "Daylight"? Berikutnya, cobalah “Cloudy” 
		dan “Shade”. Anda akan mendapatkan gambar yang nada warnanya lebih hangat.
	  </p>
