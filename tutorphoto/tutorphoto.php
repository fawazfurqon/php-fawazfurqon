		maka dengan memvariasikan keduanya akan memastikan bahwa Anda akan dapat memperoleh efek berbeda dalam foto Anda. Berikut 
		ini, kita akan menelaah 3 hal, masing-masing berkenaan dengan posisi dan sudut.
	  </p>
