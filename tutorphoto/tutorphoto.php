      <h2>#No.7 Metering</h2>
      <center><img src="images/Metering.jpg" style="height:auto;width:auto;"></img></center>
	  <h3>Apa itu <i>Metering?</i></h3>
      <p>
	     <b>Metering</b> adalah proses dalam fotografi yang bisa diotomatiskan selain <i>White Balance.</i> Jd,
		 kalau bicara soal otomatis atau manual, sebenarnya cuma soal dua ini. Metering sebenarnya 
		 sangat kecil porsinya dalam membentuk sebuah foto karena ada <b>3 faktor</b> yang lebih dominan: <b>komposisi, angle dan moment.</b>
	  </p>
	  <p>
		Fungsi <b>metering</b> (pengukuran) mengukur kecerahan subjek dan memutuskan seberapa banyak pencahayaan
		yang terbaik untuk foto yang bersangkutan. Mari kita cermati tiap mode metering (pengukuran) yang
		tersedia agar dapat lebih memahami serta mengetahui mode manakah yang terbaik untuk digunakan, dan dalam kondisi/pemandangan seperti apa
	  </p>
	  <h3>
		Fungsi metering (pengukuran) berfungsi untuk mengukur kecerahan subjek
	  </h3>
	  <h4>
		Hal yang perlu dicatat :
	  </h4>
	  <p>
		&nbsp;&nbsp;&nbsp;&nbsp;- Evaluative metering (pengukuran evaluatif) dapat digunakan untuk hampir semua pemandangan.
		<br>&nbsp;&nbsp;&nbsp;&nbsp;- Spot metering (pengukuran setempat) paling efektif apabila terdapat bagian khusus subjek
			yang ingin Anda soroti &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;pencahayaannya dengan benar.
		<br>&nbsp;&nbsp;&nbsp;&nbsp;- Tiap mode metering (pengukuran) memiliki serangkaian kondisi pemandangan/pemotretan yang
		berfungsi paling baik. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Pertimbangkan hal ini sewaktu memilih mode.
	  </p>
	  <p>
		Kamera digital memiliki fitur automatic exposure (AE / pencahayaan otomatis) yang secara otomatis 
		memutuskan seberapa banyak pencahayaan untuk gambar (yaitu, seberapa cerah bidikan jadinya).
	  </p>
	  <p>
		Dalam mode AE, apabila Anda menekan separuh tombol rana, kamera akan secara otomatis menentukan aperture dan/atau nilai kecepatan rana,
		dan karenanya memberikan pencahayaan yang tepat (ditentukan kamera). Fitur yang membantu kamera menentukan aperture dan kecepatan rana
		yang akan ditetapkan dengan mengukur kecerahan subjek, dan tindakan ini disebut "metering". Biasanya ada 3 mode metering di kamera:
		evaluative metering (pengukuran evaluatif), spot metering (pengukuran setempat) dan centre-weighted metering (pengukuran pembobotan tengah).
	  </p>
	  <p>
		Pada kebanyakan kamera, mode default-nya yaitu <b>evaluative metering (pengukuran evaluatif),</b> karena mode ini melaksanakan pengukuran 
		di seluruh gambar dan karena itu sesuai untuk segala macam pemandangan dan kondisi subjek. Sebaliknya, <b>spot metering (pengukuran setempat),</b>
		efektif apabila Anda hanya ingin memastikan bahwa area spesifik pada gambar Anda disoroti cahaya yang sesuai. Sementara itu,
		<b>centre-weighted metering (pengukuran pembobotan tengah)</b> mengukur cahaya di area tengah bingkai gambar, dan karenanya berfungsi
		paling baik apabila subjek utama yang Anda minati berada di tengah bingkai.
