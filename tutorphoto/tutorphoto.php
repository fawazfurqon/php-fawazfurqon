      <h2>#No.4 Exposure Compensation</h2>
      <center><img src="images/exposurecompensation.jpg" style="height:auto;width:auto;"></img></center>
	  <h3>Apa itu <i>Exposure Compensation?</i></h3>
      <p><i><b>Exposure Compensation</b></i> Exposure compensation adalah fungsi yang secara umum digunakan dalam <b>DSLR.</b>
           	   Fungsi ini dapat digunakan dalam fotografi cahaya latar, ketika memotret seseorang yang mengenakan pakaian 
			   warna putih atau hitam, serta di banyak situasi lainnya.
	  </p>
	  <p><i><b>Exposure Compensation</b></i> (Kompensasi pencahayaan) juga merupakan suatu fungsi yang bisa Anda gunakan untuk mengubah
     	  pencahayaan yang ditetapkan kamera (kamera menentukan pencahayaan yang tepat) menjadi sesuatu yang
		  merupakan preferensi Anda sendiri. Di sini, kita akan mengetahui lebih lanjut mengenai fungsi ini,
		  sambil belajar cara mengenali subjek yang memerlukan kompensasi pencahayaan positif atau negatif
	  </p>
	  <h3>
		Kompensasi pencahayaan digunakan untuk mengubah kecerahan gambar ("correct exposure") yang ditetapkan oleh kamera
	  </h3>
	  <h4>
		Hal yang perlu dicatat :
	  </h4>
	  <p>
		&nbsp;&nbsp;&nbsp;&nbsp;- Pencahayaan bergantung pada daya pantul subjek.
		<br>&nbsp;&nbsp;&nbsp;&nbsp;- Gunakan kompensasi pencahayaan positif apabila subjek memantulkan cahaya dengan sangat baik, dan kompensasi 
		&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;pencahayaan negatif apabila tidak memantulkan cahayanya dengan baik.
	  </p>
	  <p>
		Exposure compensation, atau kompensasi pencahayaan, adalah suatu fungsi yang memungkinkan Anda mengubah kecerahan dari pencahayaan tepat yang ditentukan kamera. 
		<br>Kamera memiliki meteran pencahayaan built-in yang menentukan pencahayaan yang "correct" (tepat) berdasarkan pada jumlah cahaya yang dipantulkan oleh subjek 
		(yaitu "subject reflectivity"). Proses ini disebut "metering". Ada beberapa cara berbeda untuk bisa mencapai ini, tetapi metering mode (mode pengukuran) yang 
		paling umum digunakan disebut “evaluative metering” (pengukuran evaluatif). Dalam mode ini, metering system (sistem pengukuran) membagi seluruh area gambar 
		ke dalam sejumlah zona dan mengukur kecerahan di tiap zona untuk menemukan nilai rata-rata. Evaluative metering efektif untuk menemukan pencahayaan tepat 
		yang ditentukan kamera dalam sebagian besar pemandangan.
	  </p>
	  <p>
		Namun demikian, pencahayaan tepat yang ditentukan kamera tidak selalu menghasilkan kecerahan optimal untuk pemandangannya. Jika subjeknya berwarna hitam 
		(atau mendekati hitam), sebagian besar kamera akan cenderung memberikan pencahayaan yang berlebihan sehingga menghasilkan gambar yang sangat terang.
		Sebaliknya, jika subjeknya berwarna putih (atau mendekati putih), sebagian besar kamera akan cenderung kurang memberikan pencahayaan sehingga menghasilkan 
		gambar yang gelap. Hal ini disebabkan karena warna putih memiliki daya pantul tinggi (memantulkan banyak cahaya), sedangkan warna hitam memiliki daya
		pantul rendah (memantulkan sedikit cahaya).
	  </p>	
	  <p>
		Di sinilah Anda bisa menggunakan kompensasi pencahayaan untuk secara manual menyesuaikan kecerahan sehingga lebih ideal. Biasanya,
		kompensasi pencahayaan ditetapkan ke posisi "0", tetapi Anda bisa menyesuaikan itu dengan dial atau tombol kompensasi pencahayaan.
		Kita biasanya mengompensasi ke arah kisaran "+" untuk subjek dengan daya pantul tinggi, dan ke arah kisaran "-" untuk subjek dengan
		daya pantul rendah. Nah, dengan begitu, tidak ada yang benar atau salah apabila menyangkut soal kompensasi pencahayaan, jadi Anda 
		juga bisa menggunakannya untuk menyetel kecerahan gambar hingga sesuai dengan maksud kreativitas atau preferensi Anda.
	  </p>
