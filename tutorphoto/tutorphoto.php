      <h2>#No.5 Kecepatan ISO</h2>
      <center><img src="images/ISO.jpg" style="height:auto;width:auto;"></img></center>
	  <h3>Apa itu <i>ISO?</i></h3>
      <p>Secara arti <b>ISO</b> atau <b>ASA</b> (dalam fotografi film) adalah kemampuan atau 
	     tingkat sensitifitas sensor pada kamera terhadap cahaya. Sebagai dasar 
	     fungsi <b>ISO</b> pada fotografi, semakin besar nilai pada setingan <b>ISO</b> kamera, 
	     maka semakin sensitif dan besar cahaya yang didapatkan.
	  </p>
	  <p>Kecepatan <b>ISO</b> memiliki peran yang sama penting seperti kecepatan aperture dan shutter mengenai
	     efeknya pada pencahayaan. Sekarang, mari kita belajar lebih lanjut mengenai keuntungan dan kerugian meningkatkan kecepatan <b>ISO</b>
	  </p>
	  <h3>
		Di lingkungan rendah cahaya, kita bisa meningkatkan kecepatan shutter (rana) dengan meninggikan kecepatan ISO
	  </h3>
	  <h4>
		Hal yang perlu dicatat :
	  </h4>
	  <p>
		&nbsp;&nbsp;&nbsp;&nbsp;- Dalam kisaran kecepatan ISO Normal, semakin rendah kecepatan ISO, semakin tinggi kualitas gambarnya.
		<br>&nbsp;&nbsp;&nbsp;&nbsp;- Meningkatkan kecepatan ISO, memungkinkan kamera menetapkan shutter speed yang lebih cepat.
		<br>&nbsp;&nbsp;&nbsp;&nbsp;- Noise terjadi pada kecepatan ISO yang lebih tinggi.
	  </p>
	  <p>
		Sederhananya, kecepatan ISO adalah kemampuan sensor gambar dalam meraba cahaya, direfleksikan sebagai nilai numerik.
		Katanya, pencahayaan menentukan baik-tidaknya sehelai gambar, tetapi kecepatan ISO juga merupakan faktor besar dalam menentukan pencahayaan.
	  </p>
	  <p>
		Jika aperture adalah lebar sinar cahaya yang melintas dan kecepatan shutter adalah waktu yang diperlukan sinar 
		cahaya untuk melintas, maka kecepatan ISO menjelaskan kemampuan sensor gambar dalam meraba cahaya. Semakin tinggi
		nilainya, semakin sensitif kamera terhadap cahaya. Bahkan, dalam lingkungan gelap atau ketika mengambil gambar 
		pemandangan malam, kita masih bisa mengambil gambar yang bagus dan cerah. Dengan kata lain, dengan berasumsi bahwa
		kita tidak memerlukan gambar yang lebih cerah, maka, kecepatan ISO yang lebih tinggi memungkinkan shutter speed yang 
		lebih cepat. Hanya dengan menyesuaikan kecepatan ISO, kita meningkatkan shutter speed (kecepatan rana). 
		Dengan melakukan hal itu, kita bisa mencegah keburaman yang disebabkan oleh goyangan kamera atau buram gerakan subjek.
	  </p>
	  <p>
		Kecepatan ISO yang lebih tinggi, juga memungkinkan kita untuk mencapai aperture yang lebih sempit tanpa mengorbankan
		kecerahan gambar, selama kita menggunakan shutter speed tetap (misalnya, dengan menggunakan mode Shutter-priority AD).
	  </p>
	  <P>
		Meskipun saya banyak sekali mengatakan bahwa kecepatan ISO merupakan fungsi yang berguna, namun juga memiliki kerugian. 
		Semakin tinggi kecepatan ISO, semakin banyak noise (butiran) yang dihasilkan. Keseluruhan gambar terlihat penuh butiran. 
		Ya, memang ada kamera dengan fitur pengurangan noise, tetapi sebagian besar fotografer akan tetap mencoba sebaik-baiknya 
		untuk menjaga kecepatan ISO pada pengaturan yang tidak cukup mencegah keburaman akibat goyangan kamera. Biasanya, hal ini
		sebanyak mungkin mendekati kecepatan ISO dasar (kecepatan ISO Normal terendah), tetapi bisa lebih tinggi, tergantung tujuan 
		fotografis dan kondisi pemotretan.
	  </p>
		
