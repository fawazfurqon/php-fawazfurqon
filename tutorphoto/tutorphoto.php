		(sudut level mata), memegang kamera menghadap ke bawah dikenal sebagai ‘<b>high angle’</b> (sudut tinggi), dan memegang kamera menghadap 
		ke atas dikenal sebagai <b>‘low angle’</b> (sudut rendah).
	  </p>
	  <p>
		Sewaktu Anda memotret, pertama-tama, amati subjek Anda secara menyeluruh sebelum memutuskan posisi pembidikannya. Berikutnya, pikirkan mengenai sudutnya.
